        }
    }

    // ── Asset scripts ─────────────────────────────────────

    /** Copy a script from APK assets to the prefix, with execute permission. */
    private fun copyAssetScript(assetName: String, dest: File) {
        try {
            context.assets.open(assetName).use { input ->
                dest.parentFile?.mkdirs()
                dest.outputStream().use { output -> input.copyTo(output) }
            }
            dest.setExecutable(true, true)
            dest.setReadable(true, true)
            Log.i(TAG, "Copied asset script: $assetName → ${dest.absolutePath}")
        } catch (e: Exception) {
            Log.w(TAG, "Failed to copy asset script: $assetName: ${e.message}")
        }
    }

    // ── Symlinks ──────────────────────────────────────────

    private fun createSymlinks(symlinks: List<Pair<String, String>>, prefix: File) {
        // Critical symlinks that must succeed for the terminal to work
        val criticalLinks = setOf("bin/sh", "bin/bash", "bin/login")
        var created = 0
        var failed = 0
        val criticalFailures = mutableListOf<String>()

        val prefixCanonical = prefix.canonicalPath + File.separator
        for ((target, linkPath) in symlinks) {
            try {
                val linkFile = File(prefix, linkPath)
                // Security: verify the link path stays within prefix (prevent path traversal via SYMLINKS.txt)
                val linkCanonicalParent = linkFile.parentFile?.canonicalPath ?: continue
                if (!linkCanonicalParent.startsWith(prefixCanonical) && linkCanonicalParent != prefix.canonicalPath) {
                    Log.w(TAG, "Symlink path escapes prefix, skipping: $linkPath")
                    failed++
                    continue
                }
                linkFile.parentFile?.mkdirs()
                linkFile.delete()
                Os.symlink(target, linkFile.absolutePath)
                created++
            } catch (e: Exception) {
                Log.w(TAG, "Symlink failed: $linkPath → $target: ${e.message}")
                failed++
                if (linkPath in criticalLinks) {
                    criticalFailures.add(linkPath)
                }
            }
        }
        Log.i(TAG, "Symlinks: $created created, $failed failed")
        if (criticalFailures.isNotEmpty()) {
            Log.e(TAG, "Critical symlinks failed: ${criticalFailures.joinToString()}")
        }
    }

